    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $validator = Validator::make($request->all(), [
            'name'     => 'required',
            'email'    => 'required|email|unique:users',
            'password' => 'required',
        ]);

        if($validator->fails()){
            return $this->sendResponse(false, 'Validation Error.', null, $validator->errors(), 200);
        }
        $new_record = User::create($request->all());
        $new_record->password = bcrypt($request->password);
        $new_record->save();
        return $this->sendResponse(true, 'Record Create successfully.', $new_record->toArray(), [], 200);
    }

